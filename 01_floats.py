# floating point number is positive or negative number containing one or more decimals
print('\nRepresenting float values')

average = 4.3

print(average) # 4.3
print(type(average)) # <class 'float'>

print(3.2) # 3.2
print(.8) # 0.8
print(5.) # 5.0
print(1_000.37) # 1000.37
print(-21.4) # 21.4

# using scientific notation, 'e' to indicate the power of 10
print(3e2) # 300.0
print(4E3) # 4000.0
print(5.2E2) # 520.0

